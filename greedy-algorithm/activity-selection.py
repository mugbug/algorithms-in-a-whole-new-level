class Task:
    def __init__(self, start, end):
        self.start = start
        self.end = end

def max_tasks(tasks):
    count = 1
    previous = 0
    for current in range(len(tasks)):
        # as the end times are sorted there is no need
        # to check if the current is the smallest one
        if tasks[current].start >= tasks[previous].end:
            count += 1
            previous = current
    print(count)

